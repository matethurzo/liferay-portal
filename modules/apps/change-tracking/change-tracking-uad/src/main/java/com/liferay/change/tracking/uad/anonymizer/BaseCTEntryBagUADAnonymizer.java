package com.liferay.change.tracking.uad.anonymizer;

import com.liferay.change.tracking.model.CTEntryBag;
import com.liferay.change.tracking.service.CTEntryBagLocalService;
import com.liferay.change.tracking.uad.constants.CTUADConstants;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;

import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the ct entry bag UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link CTEntryBagUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseCTEntryBagUADAnonymizer
	extends DynamicQueryUADAnonymizer<CTEntryBag> {
	@Override
	public void autoAnonymize(CTEntryBag ctEntryBag, long userId,
		User anonymousUser) throws PortalException {
		if (ctEntryBag.getUserId() == userId) {
			ctEntryBag.setUserId(anonymousUser.getUserId());
			ctEntryBag.setUserName(anonymousUser.getFullName());
		}

		ctEntryBagLocalService.updateCTEntryBag(ctEntryBag);
	}

	@Override
	public void delete(CTEntryBag ctEntryBag) throws PortalException {
		ctEntryBagLocalService.deleteCTEntryBag(ctEntryBag);
	}

	@Override
	public Class<CTEntryBag> getTypeClass() {
		return CTEntryBag.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return ctEntryBagLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return CTUADConstants.USER_ID_FIELD_NAMES_CT_ENTRY_BAG;
	}

	@Reference
	protected CTEntryBagLocalService ctEntryBagLocalService;
}